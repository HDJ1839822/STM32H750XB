void key_process(void)
{
 if(HAL_GPIO_ReadPin(USER_KEY_GPIO_Port,USER_KEY_Pin)==RESET)
   {
      // 按键释放时的处理
      uint32_t currentTime = HAL_GetTick();
      uint32_t pressDuration = currentTime - buttonPressTime;

       if (pressDuration<800&&pressDuration>20) {
        // 短按键处理
				 LED_R_ON;
				

      } else {
        // 长按键处理
			   buttonPressCount = 0;
				// 执行长按键事件处理
				 LED_R_OFF;

        
      }
    }

}




